# @param {String} s
# @return {String}
def reverse_vowels(s)
  vowels = ["A", "a", "E", "e", "I", "i", "O", "o", "U", "u"]





















































































































































































































































































































































  i = 0
  j = s.length - 1

  while i < j
    if vowels.include?(s[i]) && vowels.include?(s[j]) # both chars are vowels
      # swap
      temp = s[i]
      s[i] = s[j]
      s[j] = temp
      i +=1
      j -= 1
    elsif !vowels.include?(s[i])
      i += 1
    else
      j -= 1
    end
  end
  s
end